# functions

# Functions can be defined with the def keyword
def log():
  print("Hello world")

log()

# arguments can be specified in the parenthesis
def logMessage(message):
  print(message)

logMessage("Custom message")

# functions can return values
def message(message):
  return message;

# For loops can be used to iterate through a list using range
for number in range(1, 10):
  print(number)

# While loops are done until a condition is met
conditional = False

while conditional is False:
  print("Hello")
  conditional = True